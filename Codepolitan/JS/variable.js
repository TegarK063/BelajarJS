let tahunMerdeka = 1945; // variable tahunMerdeka menyimpan nilai tahun kemerdekaan Indonesia

console.log(tahunMerdeka); // menampilkan nilai tahunMerdeka ke konsol

let sekarang = 2025;
let umurIndonesia = sekarang - tahunMerdeka; // menghitung umur Indonesia
console.log(`Umur Indonesia pada tahun  ${sekarang} adalah ${umurIndonesia} tahun.`); // menampilkan umur Indonesia pada tahun sekarang